#pragma once

#include <Arduino.h>

  //////////////////////////////////////////////////
  // RC COMMAND
  //
  // ブラウザ → FCへ送る操縦データ
  //////////////////////////////////////////////////

  struct RCCommand {

  //////////////////////////////////////////////////
  // スロットル
  //
  // 範囲:
  // 0～255
  //////////////////////////////////////////////////

  uint8_t throttle;

  //////////////////////////////////////////////////
  // ヨー
  //
  // 機体回転
  //////////////////////////////////////////////////

  int16_t yaw;

  //////////////////////////////////////////////////
  // ピッチ
  //
  // 前後傾き
  //////////////////////////////////////////////////

  int16_t pitch;

  //////////////////////////////////////////////////
  // ロール
  //
  // 左右傾き
  //////////////////////////////////////////////////

  int16_t roll;

  //////////////////////////////////////////////////
  // ARM状態
  //
  // true:
  // モーター有効
  //
  // false:
  // モーター停止
  //////////////////////////////////////////////////

  bool armed;

  //////////////////////////////////////////////////
  // 最終受信時間
  //
  // フェイルセーフ判定用
  //////////////////////////////////////////////////

  uint32_t lastUpdateMs;
};

//////////////////////////////////////////////////
// グローバル共有RCデータ
//
// volatile:
//
// 他タスクから変更される可能性あり
//////////////////////////////////////////////////

extern volatile RCCommand g_rc;

