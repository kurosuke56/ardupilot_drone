//////////////////////////////////////////////////
// TELEMETRY DATA
//
// FC → ブラウザへ送る状態データ
//////////////////////////////////////////////////

struct TelemetryData {

  //////////////////////////////////////////////////
  // 各モーター出力値
  //
  // 範囲:
  // 0～255
  //////////////////////////////////////////////////

  uint8_t m1;
  uint8_t m2;
  uint8_t m3;
  uint8_t m4;

  //////////////////////////////////////////////////
  // ARM状態
  //////////////////////////////////////////////////

  bool armed;

  //////////////////////////////////////////////////
  // WiFi電波強度
  //
  // 単位:
  // dBm
  //
  // 目安:
  //
  // -30 非常に強い
  // -50 良好
  // -70 普通
  // -90 弱い
  //////////////////////////////////////////////////

  int32_t rssi;
};

//////////////////////////////////////////////////
// グローバル共有テレメトリ
//////////////////////////////////////////////////

extern volatile TelemetryData g_tel;

