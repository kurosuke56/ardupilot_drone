// 【役割】
//
// タスク間共有データ定義
//
// FreeRTOSでは複数タスクが同時に動作するため
// 共通データをここで管理する
//
// 使用例
//
// Browser
//     ↓
// g_rc
//     ↓
// FlightController
//
// FlightController
//     ↓
// g_tel
//     ↓
// Browser
//
