//
// FlightController.cpp
//
// 【役割】
//
// RC入力を元に4つのモーター出力を決定
//
// 処理内容
//
// ・PWM初期化
// ・モーター制御
// ・Mixer計算
// ・フェイルセーフ
//
//////////////////////////////////////////////////

#include "FlightController.h"

//////////////////////////////////////////////////
// PINS
//////////////////////////////////////////////////

// モーター配置
//
//       Front
//
//   M4          M1
//
//   M3          M2
//
//       Rear
//

#define M1_PIN D0
#define M2_PIN D1
#define M3_PIN D8
#define M4_PIN D9

// モータードライバ有効化
#define STBY_PIN D10

//////////////////////////////////////////////////
// PWM
//////////////////////////////////////////////////

// PWM周波数
#define PWM_FREQ 20000

// PWM分解能
//
// 8bit
// 0～255
//
#define PWM_RES 8

//////////////////////////////////////////////////
// TIMING
//////////////////////////////////////////////////

// 通信断時の停止時間
//
// 通信が300ms途絶えたら
// モーター停止
//
#define FAILSAFE_TIMEOUT_MS 300

  //////////////////////////////////////////////////
  // INITIALIZE
  //////////////////////////////////////////////////

  void
  FlightController::begin() {

  //////////////////////////////////////////////////
  // モータードライバ有効化
  //////////////////////////////////////////////////

  pinMode(
    STBY_PIN,
    OUTPUT);

  digitalWrite(
    STBY_PIN,
    HIGH);

  //////////////////////////////////////////////////
  // PWM設定
  //////////////////////////////////////////////////

  ledcAttach(
    M1_PIN,
    PWM_FREQ,
    PWM_RES);

  ledcAttach(
    M2_PIN,
    PWM_FREQ,
    PWM_RES);

  ledcAttach(
    M3_PIN,
    PWM_FREQ,
    PWM_RES);

  ledcAttach(
    M4_PIN,
    PWM_FREQ,
    PWM_RES);

  //////////////////////////////////////////////////
  // 起動時は停止状態
  //////////////////////////////////////////////////

  stopMotors();
}

//////////////////////////////////////////////////
// MOTOR OUTPUT
//////////////////////////////////////////////////

void FlightController::writeMotor(
  uint8_t pin,
  uint8_t pwm) {

  // PWM出力
  ledcWrite(
    pin,
    pwm);
}

//////////////////////////////////////////////////
// MOTOR STOP
//////////////////////////////////////////////////

void FlightController::stopMotors() {

  //////////////////////////////////////////////////
  // 内部状態クリア
  //////////////////////////////////////////////////

  motorOutput[0] = 0;
  motorOutput[1] = 0;
  motorOutput[2] = 0;
  motorOutput[3] = 0;

  //////////////////////////////////////////////////
  // モーター停止
  //////////////////////////////////////////////////

  writeMotor(M1_PIN, 0);
  writeMotor(M2_PIN, 0);
  writeMotor(M3_PIN, 0);
  writeMotor(M4_PIN, 0);

  //////////////////////////////////////////////////
  // テレメトリ更新
  //////////////////////////////////////////////////

  g_tel.m1 = 0;
  g_tel.m2 = 0;
  g_tel.m3 = 0;
  g_tel.m4 = 0;
}

//////////////////////////////////////////////////
// MIXER
//
// RC入力を4モーターへ変換
//
// throttle : 上下
// yaw      : 回転
// pitch    : 前後
// roll     : 左右
//
//////////////////////////////////////////////////

void FlightController::updateMixer(
  const RCCommand& rc) {

  //////////////////////////////////////////////////
  // ARM解除なら停止
  //////////////////////////////////////////////////

  if (!rc.armed) {

    stopMotors();

    return;
  }

  int throttle =
    rc.throttle;

  int yaw =
    rc.yaw;

  int pitch =
    rc.pitch;

  int roll =
    rc.roll;

  //////////////////////////////////////////////////
  // Mixer計算
  //////////////////////////////////////////////////

  int m1 = throttle - pitch - roll - yaw;
  int m2 = throttle + pitch - roll + yaw;
  int m3 = throttle + pitch + roll - yaw;
  int m4 = throttle - pitch + roll + yaw;

  //////////////////////////////////////////////////
  // PWM範囲制限
  //////////////////////////////////////////////////

  motorOutput[0] =
    constrain(m1, 0, 255);

  motorOutput[1] =
    constrain(m2, 0, 255);

  motorOutput[2] =
    constrain(m3, 0, 255);

  motorOutput[3] =
    constrain(m4, 0, 255);

  //////////////////////////////////////////////////
  // モーター出力
  //////////////////////////////////////////////////

  writeMotor(M1_PIN, motorOutput[0]);
  writeMotor(M2_PIN, motorOutput[1]);
  writeMotor(M3_PIN, motorOutput[2]);
  writeMotor(M4_PIN, motorOutput[3]);

  //////////////////////////////////////////////////
  // テレメトリ更新
  //////////////////////////////////////////////////

  g_tel.m1 =
    motorOutput[0];

  g_tel.m2 =
    motorOutput[1];

  g_tel.m3 =
    motorOutput[2];

  g_tel.m4 =
    motorOutput[3];

  g_tel.armed =
    rc.armed;

  g_tel.rssi =
    WiFi.RSSI();
}

//////////////////////////////////////////////////
// UPDATE
//////////////////////////////////////////////////

void FlightController::update() {

  //////////////////////////////////////////////////
  // 共有データをローカルへコピー
  //////////////////////////////////////////////////

  RCCommand rc;

  rc.throttle =
    g_rc.throttle;

  rc.yaw =
    g_rc.yaw;

  rc.pitch =
    g_rc.pitch;

  rc.roll =
    g_rc.roll;

  rc.armed =
    g_rc.armed;

  rc.lastUpdateMs =
    g_rc.lastUpdateMs;

  //////////////////////////////////////////////////
  // フェイルセーフ
  //////////////////////////////////////////////////

  if (
    millis()
      - rc.lastUpdateMs
    > FAILSAFE_TIMEOUT_MS) {

    stopMotors();

    g_rc.armed =
      false;
  } else {

    updateMixer(
      rc);
  }
}

